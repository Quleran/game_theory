import numpy as np

# Входные данные
k = 4                 # число каналов обслуживания
n = 18                # число источников заявок
lam = 1.8             # интенсивность поступления от одного активного источника, заявок/час
t = 0.5               # среднее время обслуживания одной заявки, часы
mu = 1 / t            # интенсивность обслуживания одного канала, заявок/час

print("Исходные данные:")
print(f"k = {k}, n = {n}, λ = {lam} заявок/час, t = {t} ч, μ = {mu} заявок/час\n")

# 1. Расчёт финальных вероятностей P_m (m = 0..n)
# Вычисляем произведение отношений λ_i / μ_{i+1} для каждого m
# Используем рекуррентную формулу: P[m] = P[0] * prod_{i=0}^{m-1} (λ_i / μ_{i+1})
# Сначала найдём нормировочную константу

lambda_arr = [lam * (n - m) for m in range(n)]      # λ_m для m = 0..n-1
mu_arr = [0] + [m * mu if m <= k else k * mu for m in range(1, n+1)]  # μ_m для m = 1..n

# Вычисляем последовательность произведений
prod = 1.0
prods = [1.0]   # prods[m] = prod_{i=0}^{m-1} (λ_i / μ_{i+1}), prods[0]=1
for m in range(1, n+1):
    prod *= lambda_arr[m-1] / mu_arr[m]
    prods.append(prod)

# Суммируем все prods для нормировки
sum_prod = sum(prods)
P0 = 1.0 / sum_prod

# Вероятности состояний
P = [P0 * prod for prod in prods]

print("1. Предельные вероятности состояний СМО:")
for m in range(n+1):
    print(f"   P_{m} = {P[m]:.6f}")

# 2. Вероятность того, что все каналы свободны (система пуста)
P_free_all = P[0]
print(f"\n2. Вероятность, что все каналы свободны: P_0 = {P_free_all:.6f}")

# 3. Среднее число заявок в очереди L_q
L_q = sum((m - k) * P[m] for m in range(k+1, n+1))
print(f"\n3. Среднее число заявок в очереди: L_q = {L_q:.6f}")

# 4. Среднее число заявок в системе L
L = sum(m * P[m] for m in range(n+1))
print(f"4. Среднее число заявок в системе: L = {L:.6f}")

# 5. Среднее число свободных каналов
free_channels = sum((k - m) * P[m] for m in range(k+1))
print(f"5. Среднее число свободных каналов: M_св = {free_channels:.6f}")

# 6. Среднее число занятых каналов (среднее число обслуживаемых заявок)
busy_channels = sum(min(m, k) * P[m] for m in range(n+1))
print(f"6. Среднее число занятых каналов: M_зан = {busy_channels:.6f}")

# 7. Абсолютная пропускная способность A (среднее число обслуженных заявок в час)
A = mu * busy_channels
print(f"7. Абсолютная пропускная способность: A = {A:.6f} заявок/час")

# 8. Относительная пропускная способность Q
# Все поступившие заявки рано или поздно обслуживаются, поэтому Q = 1.
# Можно вычислить как A / (λ * (n - L)) = 1.
Q = 1.0
print(f"8. Относительная пропускная способность: Q = {Q} (все заявки обслуживаются)")

# 9. Вероятность наличия очереди
P_queue = sum(P[m] for m in range(k+1, n+1))
print(f"9. Вероятность наличия очереди: P_оч = {P_queue:.6f}")

# 10. Среднее время пребывания в очереди, под обслуживанием и в системе (по формулам Литтла)
W_q = L_q / A if A > 0 else 0
W = L / A if A > 0 else 0
W_service = 1 / mu   # среднее время обслуживания одной заявки
print(f"\n10. Среднее время пребывания:")
print(f"    - в очереди: W_q = {W_q:.6f} ч")
print(f"    - под обслуживанием: W_обс = {W_service:.6f} ч")
print(f"    - в системе: W = {W:.6f} ч")
print(f"    (Проверка: W_q + W_обс = {W_q + W_service:.6f} ч ≈ W)")