import numpy as np

# Исходные данные
lambda_ = 0.5  # интенсивность поступления заявок (заявок/мин)
t = 3  # среднее время обслуживания (мин)
k = 2  # число каналов обслуживания
omega = 10  # максимальное время ожидания в очереди (мин)
C = 150  # доход от одной обслуженной заявки (у.е.)
eps = 0.01  # требуемая точность для вероятностей

# Производные параметры
mu = 1 / t  # интенсивность обслуживания одного канала (заявок/мин)
nu = 1 / omega  # интенсивность ухода заявки из очереди (1/мин)

print("[Параметры системы]")
print(f"λ = {lambda_} заявок/мин")
print(f"t = {t} мин -> μ = {mu:.4f} заявок/мин")
print(f"k = {k} каналов")
print(f"ω = {omega} мин -> ν = {nu:.4f} 1/мин")
print(f"C = {C} у.е./заявка")
print(f"ε = {eps}\n")

#  1. Предельные вероятности состояний
# Система является процессом рождения-гибели с интенсивностями:

p = [1.0]  # p[0] = 1 (временное значение)
i = 1
while True:
    # Вычисляем μ_i для текущего i
    if i <= k:
        mu_i = i * mu
    else:
        mu_i = k * mu + (i - k) * nu

    # Рекуррентная формула p_i = p_{i-1} * λ / μ_i
    p_i = p[-1] * (lambda_ / mu_i)
    p.append(p_i)

    # Условие остановки: если очередная вероятность очень мала, дальнейшие будут ещё меньше
    # и их суммарный вклад < ε (гарантируется для убывающей геометрической прогрессии)
    if p_i < eps / 100 and i > k:
        break
    i += 1
    if i > 1000:  # предохранитель
        break

# Нормировка: сумма всех p_i должна равняться 1
total = sum(p)
p = [pi / total for pi in p]

print("[Предельные вероятности состояний]")
print("Состояние (число заявок) : Вероятность")
for i, pi in enumerate(p):
    if pi > eps / 10:  # выводим только значимые вероятности
        print(f"  {i:3d}                    : {pi:.6f}")
print(f"Сумма вероятностей = {sum(p):.6f}\n")

#  2. Основные характеристики СМО
# Вероятность потери заявки (уход из очереди)
# Интенсивность ухода из очереди
loss_intensity = 0.0
L_q = 0.0  # среднее число заявок в очереди
L = 0.0  # среднее число заявок в системе

for i, pi in enumerate(p):
    L += i * pi
    if i > k:
        n_q = i - k
        L_q += n_q * pi
        loss_intensity += n_q * nu * pi

P_loss = loss_intensity / lambda_  # доля потерянных заявок
P_serv = 1 - P_loss  # вероятность обслуживания

# Эффективная интенсивность обслуженных заявок (поток, действительно прошедший обслуживание)
lambda_eff = lambda_ * P_serv

# Среднее время пребывания в системе (для обслуженных заявок)
# По формуле Литтла: L = lambda_eff * W  =>  W = L / lambda_eff
if lambda_eff > 0:
    W = L / lambda_eff
    W_q = L_q / lambda_eff
else:
    W = float('inf')
    W_q = float('inf')

# Среднее время обслуживания для обслуженной заявки = 1/μ = t
W_s = t

print("[Основные характеристики СМО]")
print(f"Вероятность обслуживания заявки:         {P_serv:.6f}")
print(f"Вероятность потери (ухода) заявки:       {P_loss:.6f}")
print(f"Среднее число заявок в очереди L_q:       {L_q:.6f}")
print(f"Среднее число заявок в системе L:         {L:.6f}")
print(f"Среднее время ожидания в очереди W_q:     {W_q:.4f} мин")
print(f"Среднее время обслуживания W_s:           {W_s:.4f} мин")
print(f"Среднее время пребывания в системе W:     {W:.4f} мин")
print(f"Эффективная интенсивность потока λ_eff:   {lambda_eff:.4f} заявок/мин\n")

# 3. Оценка средних потерь дохода
# Потери возникают из-за того, что заявки уходят из очереди не обслуженными.
# Интенсивность ухода = Σ (i-k)*ν * p_i = loss_intensity.
# Тогда средние потери дохода в единицу времени:
D_loss_1 = C * loss_intensity  # у.е./мин
# Альтернативная формула, использующая L_q и ω (по смыслу: v_уход = L_q / ω)
D_loss_2 = C * (L_q / omega)

print("[Потери дохода]")
print(f"Интенсивность ухода заявок: {loss_intensity:.4f} заявок/мин")
print(f"Средние потери дохода (через интенсивность ухода):   {D_loss_1:.2f} у.е./мин")
print(f"Средние потери дохода (через L_q/ω):                 {D_loss_2:.2f} у.е./мин")
print("Примечание: в задании приведена формула D_потерь = C·v_уход, где v_уход = ω·L_оч,")
print("но такая размерность не соответствует смыслу интенсивности ухода. Корректная формула: v_уход = L_оч / ω.")
print("Поэтому в отчёте следует использовать D_loss = C·(L_q/ω).\n")

# Дополнительная проверка: вычисление вероятности, что система свободна
print("[Проверка точности]")
print(f"Вероятность свободной системы p0 = {p[0]:.6f}")
print(f"Суммарный хвост вероятностей (не выведенные состояния) ≈ {1 - sum(p[:len(p)]) if len(p) < 200 else 'мал'}")
